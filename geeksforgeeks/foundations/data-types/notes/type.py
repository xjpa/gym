# type()
# built-in function that returns the data type of a variable/value

a = 10
print(type(a)) # int

b = 10.5
print(type(b)) # float

c = 2 + 4j # complex number
print(type(c)) # complex


# sequence types (collections)

str = "John"
print(type(str)) # str

l = [10, 20, 30]
print(type(l)) # list

t = (10, 20, 30)
print(type(t)) # tuple

s = {10, 20, 30}
print(type(s)) # set

d = {10: "gfg", 20: "ide"}
print(type(d)) # dict
